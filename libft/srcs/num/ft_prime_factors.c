#include "libft.h"

void	ft_prime_factors(int n)
{
	int		i;

	if (n == 1)
	{
		ft_printf("1");
		return ;
	}
	i = 2;
	while (n / i != 1)
	{
		if (n % i == 0)
		{
			ft_printf("%d*", i);
			n /= i;
			i--;
		}
		i++;
	}
	ft_printf("%d", n);
}
