#include "ft_num.h"

int	ft_numlen(intmax_t n)
{
	int			len;
	uintmax_t	un;

	if (n == 0)
		return (1);
	len = 0;
	if (n <= 0)
	{
		un = -n;
		len++;
	}
	else
		un = n;
	while (un > 0)
	{
		un /= 10;
		len++;
	}
	return (len);
}
