#include "libft.h"

void	ft_strcap(char s[])
{
	int i;

	if (ft_isalpha(s[0]))
		s[0] -= 32;
	i = 1;
	while (s[i])
	{
		if (ft_isupper(s[i]))
			s[i] += 32;
		if (ft_islower(s[i]) && (s[i - 1] >= ' ' && s[i - 1] <= '/'))
			s[i] -= 32;
		++i;
	}
}
